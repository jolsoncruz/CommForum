function getRandomProfessors (count) {
    const randomProfessors = [];
    const professorsIndices = new Set();
    const maxIndex = professorModel.length;

    // Generate unique random indices
    while (professorsIndices.size < count) {
        const randomIndex = Math.floor(Math.random() * maxIndex);
        professorsIndices.add(randomIndex);
    }

    // Retrieve reviews using random indices
    for (const index of professorsIndices) {
        randomProfessors.push(professorModel[index]);
    }

    return randomProfessors;
}

